/*
 * Generate a pattern using the given bist_line_pattern array.
 * Return size of pattern generated.
 */
static int get_patt_by_line(uint8_t *pattern,
			    struct bist_line_pattern *line_patt,
			    int line_patt_len)
{
	int pattern_size = 0;

	/*
	 * For each line, we search for the entry in line_bist which indicates
	 * its pattern and copy it to the *pattern area. We stop until we
	 * reach the maximum pattern size or we couldn't find an entry for the
	 * line.
	 */
	for (int line = 0; line < MAX_BIST_LINES; line++) {
		int line_found = 0;
		int use_map_idx = line / (8 * sizeof(uint64_t));
		uint64_t use_map_bit = 1ull << (line % (8 * sizeof(uint64_t)));
		for (int i = 0; i < line_patt_len; i++)
			if (line_patt[i].use_map[use_map_idx] & use_map_bit) {
				line_found = 1;
				pattern_size += PATTERN_LINE_LENGTH;
				memcpy(pattern, line_patt[i].line_pattern,
				       PATTERN_LINE_LENGTH);
				pattern += PATTERN_LINE_LENGTH;
				break;
			}
		if (!line_found)
			break;
	}

	return pattern_size;
}
