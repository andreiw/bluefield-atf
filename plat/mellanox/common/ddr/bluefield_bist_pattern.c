struct bist_genereated_pattern gen_pattern = {
	.pattern_ppointer = NULL,
};

/* Fill in the dynamically generated pattern, return 0 if succussful. */
int fill_pattern(enum bist_pattern_type type)
{
	/* Check that we need to generate the pattern. */
	if (bist_configs[type].pattern != NULL)
		return 0;

	/* Check that the pattern have a generating function. */
	if (bist_configs[type].get_pattern == NULL)
		return 1;

	/* Remove the previous pattern's usage of the space. */
	if (gen_pattern.pattern_ppointer != NULL)
		*gen_pattern.pattern_ppointer = NULL;

	/* Generate the new pattern using the generate function. */
	bist_configs[type].get_pattern(gen_pattern.pattern);

	/* Mark the generated pattern to that pattern. */
	bist_configs[type].pattern = gen_pattern.pattern;
	gen_pattern.pattern_ppointer = &bist_configs[type].pattern;

	return 0;
}
